    private void tbl_khachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_khachHangMouseClicked
        // TODO add your handling code here:
        int row = tbl_khachHang.getSelectedRow();
    if (row >= 0) {
        txt_maKhachHang.setText(tbl_khachHang.getValueAt(row, 0).toString());
        txt_ten.setText(tbl_khachHang.getValueAt(row, 1).toString());
        txt_soDienThoai.setText(tbl_khachHang.getValueAt(row, 2).toString());
        boolean gt = tbl_khachHang.getValueAt(row, 3).toString().equals("Nam");
        jRadioButton2.setSelected(gt);
        jRadioButton1.setSelected(!gt);
        txt_email.setText(tbl_khachHang.getValueAt(row, 4).toString());
    }

    }//GEN-LAST:event_tbl_khachHangMouseClicked

