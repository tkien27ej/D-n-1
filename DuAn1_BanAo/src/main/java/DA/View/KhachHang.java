    private void loadTable() {
    KhachHangDAO dao = new KhachHangDAO();
    List<KhachHangModel> list = dao.getAll();

        DefaultTableModel model = (DefaultTableModel) tbl_khachHang.getModel();
    model.setRowCount(0);

    for (KhachHangModel kh : list) {
    model.addRow(new Object[]{
        kh.getMaKhachHang(),
        kh.getTen(),
        kh.getSdt(),
        kh.isGioiTinh() ? "Nam" : "Nữ",
        kh.getEmail()
    });
}
    System.out.println("Số lượng KH lấy được từ DB = " + list.size());


}
    
